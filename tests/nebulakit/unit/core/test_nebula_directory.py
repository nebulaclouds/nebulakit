def test_directory_guess():
    transformer = TypeEngine.get_transformer(NebulaDirectory)
    lt = transformer.get_literal_type(NebulaDirectory["txt"])
    assert lt.blob.format == "txt"
    assert lt.blob.dimensionality == 1

    fft = transformer.guess_python_type(lt)
    assert issubclass(fft, NebulaDirectory)
    assert fft.extension() == "txt"

    lt = transformer.get_literal_type(NebulaDirectory)
    assert lt.blob.format == ""
    assert lt.blob.dimensionality == 1

    fft = transformer.guess_python_type(lt)
    assert issubclass(fft, NebulaDirectory)
    assert fft.extension() == ""
