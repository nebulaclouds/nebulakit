def test_transformer_to_literal_local():
    random_dir = context_manager.NebulaContext.current_context().file_access.get_random_local_directory()
    fs = FileAccessProvider(local_sandbox_dir=random_dir, raw_output_prefix=os.path.join(random_dir, "raw"))
    ctx = context_manager.NebulaContext.current_context()
    with context_manager.NebulaContextManager.with_context(ctx.with_file_access(fs)) as ctx:
        # Use a separate directory that we know won't be the same as anything generated by nebulakit itself, lest we
        # accidentally try to cp -R /some/folder /some/folder/sub which causes exceptions obviously.
        p = "/tmp/nebula/test_fd_transformer"

        # Create an empty directory and call to literal on it
        if os.path.exists(p):
            shutil.rmtree(p)
        pathlib.Path(p).mkdir(parents=True)

        tf = NebulaDirToMultipartBlobTransformer()
        lt = tf.get_literal_type(NebulaDirectory)
        literal = tf.to_literal(ctx, NebulaDirectory(p), NebulaDirectory, lt)
        assert literal.scalar.blob.uri.startswith(random_dir)

        # Create a NebulaDirectory where remote_directory is False
        literal = tf.to_literal(ctx, NebulaDirectory(p, remote_directory=False), NebulaDirectory, lt)
        assert literal.scalar.blob.uri.startswith(p)

        # Create a director with one file in it
        if os.path.exists(p):
            shutil.rmtree(p)
        pathlib.Path(p).mkdir(parents=True)
        with open(os.path.join(p, "xyz"), "w") as fh:
            fh.write("Hello world\n")
        literal = tf.to_literal(ctx, NebulaDirectory(p), NebulaDirectory, lt)

        mock_remote_files = os.listdir(literal.scalar.blob.uri)
        assert mock_remote_files == ["xyz"]

        # The only primitives allowed are strings
        with pytest.raises(AssertionError):
            tf.to_literal(ctx, 3, NebulaDirectory, lt)

        with pytest.raises(TypeError, match="No automatic conversion from <class 'int'>"):
            TypeEngine.to_literal(ctx, 3, NebulaDirectory, lt)


